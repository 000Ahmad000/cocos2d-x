    /** @~english Gets how many seconds the emitter will run. -1 means 'forever'.
     * @~chinese 获取发射器运行时间。
     * 
     * @return @~english The seconds that the emitter will run. -1 means 'forever'.
     * @~chinese 发射器运行时间（单位秒），-1表示一直运行。
     */
